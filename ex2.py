import turtle

leo = turtle.Turtle()   #Creating the leo

leo.forward(50)
leo.right(90)      # Rotate clockwise by 90 degrees

leo.forward(50)
leo.right(90)      # Rotate clockwise by 90 degrees
 
leo.forward(50)
leo.right(90)       # Rotate clockwise by 90 degrees

leo.forward(50)
leo.right(90)        #Rotate clockwise by 90 degrees 

turtle.done()       #Finish the program
